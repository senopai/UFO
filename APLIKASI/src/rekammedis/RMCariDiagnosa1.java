    private void tbKamarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbKamarMouseClicked
        if(tabMode.getRowCount()!=0){
        try {
            int row = tbKamar.getSelectedRow();
            int col = tbKamar.getSelectedColumn();
            
            if(col == 0){
                Object currentValue = tbKamar.getValueAt(row, 0);
                boolean isChecked = currentValue != null && Boolean.TRUE.equals(currentValue);
                boolean newValue = !isChecked;
                tbKamar.setValueAt(newValue, row, 0);
                
                if(newValue){
                    int jumlahAktif = 0;
                    int maxUrut = 0;
                    for(int i=0; i<tbKamar.getRowCount(); i++){
                        Object valUrut = tbKamar.getValueAt(i, 4);
                        if(valUrut != null && !valUrut.toString().equals("")){
                            jumlahAktif++;
                            try{
                                int n = Integer.parseInt(valUrut.toString());
                                if(n > maxUrut) maxUrut = n;
                            }catch(Exception e){}
                        }
                    }

                    if(jumlahAktif >= 5){
                        javax.swing.JOptionPane.showMessageDialog(null, "Maaf, maksimal hanya boleh memilih 5 diagnosa!");
                        tbKamar.setValueAt(false, row, 0); 
                        return;
                    }

                    tbKamar.setValueAt(String.valueOf(maxUrut + 1), row, 4);
                    
                } else {
                    String valUrut = (String) tbKamar.getValueAt(row, 4);
                    int urutanDihapus = 0;
                    if(valUrut != null && !valUrut.equals("")){
                        try{ urutanDihapus = Integer.parseInt(valUrut); }catch(Exception e){}
                    }
                    
                    tbKamar.setValueAt("", row, 4);
                    
                    if(urutanDihapus > 0){
                        for(int i=0; i<tbKamar.getRowCount(); i++){
                            Object valObj = tbKamar.getValueAt(i, 4);
                            if(valObj != null && !valObj.toString().equals("")){
                                try{
                                    int nilai = Integer.parseInt(valObj.toString());
                                    if(nilai > urutanDihapus){
                                        tbKamar.setValueAt(String.valueOf(nilai - 1), i, 4);
                                    }
                                }catch(Exception e){}
                            }
                        }
                    }
                }
            }
        } catch (Exception e) {
            System.out.println("Error : " + e);
        }
    }
}//GEN-LAST:event_tbKamarMouseClicked

