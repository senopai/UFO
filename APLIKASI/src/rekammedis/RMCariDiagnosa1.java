    public void tampil() {
        Valid.tabelKosong(tabMode);
        try {
            if (norawat.equals("")) {
                return;
            }

            ps = koneksi.prepareStatement(
                "select kd_diagnosa_utama, diagnosa_utama, " +
                "kd_diagnosa_sekunder, diagnosa_sekunder, " +
                "kd_diagnosa_sekunder2, diagnosa_sekunder2, " +
                "kd_diagnosa_sekunder3, diagnosa_sekunder3, " +
                "kd_diagnosa_sekunder4, diagnosa_sekunder4 " +
                "from resume_pasien_ranap where no_rawat=?");

            try {
                ps.setString(1, norawat);
                rs = ps.executeQuery();
                nomorUrut = 1;

                if (rs.next()) {
                    addDiagnosaToRow(rs.getString("kd_diagnosa_utama"), rs.getString("diagnosa_utama"), "Diagnosa Utama");
                    addDiagnosaToRow(rs.getString("kd_diagnosa_sekunder"), rs.getString("diagnosa_sekunder"), "Diagnosa Sekunder 1");
                    addDiagnosaToRow(rs.getString("kd_diagnosa_sekunder2"), rs.getString("diagnosa_sekunder2"), "Diagnosa Sekunder 2");
                    addDiagnosaToRow(rs.getString("kd_diagnosa_sekunder3"), rs.getString("diagnosa_sekunder3"), "Diagnosa Sekunder 3");
                    addDiagnosaToRow(rs.getString("kd_diagnosa_sekunder4"), rs.getString("diagnosa_sekunder4"), "Diagnosa Sekunder 4");
                }
            } catch (Exception ex) {
                System.out.println("Error Query: " + ex);
            } finally {
                if (rs != null) { rs.close(); }
                if (ps != null) { ps.close(); }
            }
        } catch (Exception e) {
            System.out.println("Notifikasi : " + e);
        }
        LCount.setText("" + tabMode.getRowCount());
    }

    private void addDiagnosaToRow(String kode, String nama, String statusLabel) {
        boolean kodeAda = kode != null && !kode.trim().equals("");
        boolean namaAda = nama != null && !nama.trim().equals("");

        if (kodeAda || namaAda) { 
            String kodeTampil = (kode == null) ? "" : kode.trim();
            String namaTampil = (nama == null) ? "" : nama.trim();
            String keyword = TCari.getText().trim().toLowerCase();
            String content = (kodeTampil + " " + namaTampil).toLowerCase();

            if (keyword.equals("") || content.contains(keyword)) {
                tabMode.addRow(new Object[]{
                    Boolean.FALSE,  
                    kodeTampil,     
                    namaTampil,     
                    statusLabel,    
                    ""              
                });
            }
        }
    }

